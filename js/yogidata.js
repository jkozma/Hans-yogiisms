var i = 0;
function showimg() {
    i = Math.floor((Math.random() * hquot.length));
    document.getElementById("imgdiv").style.backgroundImage = "url("+himg[i]+")";
    document.getElementById("quotspan").innerHTML = hquot[i];
    document.getElementById("credspan").innerHTML = "Picture credit (click)";
}

function show_link() {
  document.getElementById("credspan").style.textDecoration = "underline";
  document.getElementById("credspan").style.color = "blue";
}
function show_credit() {
  document.getElementById("credspan").innerHTML = hcred[i];
  document.getElementById("credspan").style.textDecoration = "none";
  document.getElementById("credspan").style.color = "black";
}
function hide_credit() {
  document.getElementById("credspan").innerHTML = "Picture credit (click)";
  document.getElementById("credspan").style.textDecoration = "none";
  document.getElementById("credspan").style.color = "black";
}
